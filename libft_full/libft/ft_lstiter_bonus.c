#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*current;

	if (!lst || !f)
		return ;
	current = lst;
	while (1)
	{
		f(current->content);
		if (!current->next)
			break ;
		else
			current = current->next;
	}
}
